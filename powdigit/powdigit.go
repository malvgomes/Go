func multOfSlices(a int, b [400]int) [400]int {
	c := [400]int{}
	resto := 0

	k, j := 0, 0
	for j < len(b) {
		mult := a*b[j] + resto
		if mult > 9 {
			c[k] = mult % 10
			resto = mult / 10
		} else {
			c[k] = mult
			resto = 0
		}
		j++
		k++
	}

	return c
}

func reverse(numbers [400]int) [400]int {
	for i, j := 0, len(numbers)-1; i < j; i, j = i+1, j-1 {
		numbers[i], numbers[j] = numbers[j], numbers[i]
	}
	return numbers
}

func sumDigits(arr [400]int) int {
	sum := 0
	for i := range arr {
		sum += arr[i]
	}

	return sum
}

